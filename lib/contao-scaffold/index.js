// SQL GENERATOR — Theme, Layout, Content
// ============================================================

export function generateContentSQL(sections, projectName, googleFontsUrl = null, adminEmail = 'info@robinherbeck.com') {
  const statements = ['SET NAMES utf8mb4;']

  // CSS-Link als head-Tag fuer das Layout (Google Fonts + theme.css)
  let headTags = ''
  if (googleFontsUrl) {
    headTags += `<link rel="preconnect" href="https://fonts.googleapis.com"><link rel="preconnect" href="https://fonts.gstatic.com" crossorigin><link rel="stylesheet" href="${googleFontsUrl}">`
  }

  // Theme erstellen
  statements.push(
    `DELETE FROM tl_theme WHERE id = 1;`,
    `INSERT INTO tl_theme (id, name, author, tstamp) VALUES (1, ${sqlEsc('Starter Theme')}, ${sqlEsc('Deploy Dashboard')}, UNIX_TIMESTAMP());`,
  )

  // Layout erstellen — external CSS + JS ueber head-Tag einbinden
  const cssHeadTag = `<link rel="stylesheet" href="files/theme/style.css">`
  const jsHeadTag = `<script defer src="files/theme/main.js"></script>`
  const allHeadTags = headTags + cssHeadTag + jsHeadTag

  // modules: Serialisiertes PHP-Array — Artikel-Modul (mod=0) in Hauptspalte (col=main)
  // Ohne dieses Feld wirft Contao einen foreach-Fehler in PageRegular.php
  const modules = 'a:1:{i:0;a:3:{s:3:\\"mod\\";s:1:\\"0\\";s:3:\\"col\\";s:4:\\"main\\";s:6:\\"enable\\";s:1:\\"1\\";}}'
  const emptyArray = 'a:0:{}'

  statements.push(
    `DELETE FROM tl_layout WHERE id = 1;`,
    `INSERT INTO tl_layout (id, pid, name, template, head, modules, sections, external, externalJs, tstamp) VALUES (1, 1, ${sqlEsc('Standard')}, 'fe_page', ${sqlEsc(allHeadTags)}, '${modules}', '${emptyArray}', '${emptyArray}', '${emptyArray}', UNIX_TIMESTAMP());`,
  )

  // Seitenstruktur erstellen (contao:migrate legt nur Tabellen an, keine Seiten)
  // Root-Seite
  statements.push(
    `DELETE FROM tl_page WHERE id IN (1, 2);`,
    `INSERT INTO tl_page (id, pid, sorting, tstamp, title, alias, type, language, fallback, includeLayout, layout, published, adminEmail) VALUES (1, 0, 128, UNIX_TIMESTAMP(), ${sqlEsc(projectName)}, 'root', 'root', 'de', 1, 1, 1, 1, ${sqlEsc(adminEmail)});`,
  )

  // Startseite (regulaere Seite unter Root)
  statements.push(
    `INSERT INTO tl_page (id, pid, sorting, tstamp, title, alias, type, published) VALUES (2, 1, 128, UNIX_TIMESTAMP(), 'Startseite', 'startseite', 'regular', 1);`,
  )

  // Artikel fuer Startseite
  statements.push(
    `DELETE FROM tl_article WHERE pid = 2;`,
    `INSERT INTO tl_article (id, pid, sorting, tstamp, title, alias, inColumn, published) VALUES (1, 2, 128, UNIX_TIMESTAMP(), 'Startseite', 'startseite', 'main', 1);`,
  )

  // Bestehende Content-Elemente loeschen
  statements.push(
    `DELETE FROM tl_content WHERE pid = 1 AND ptable = 'tl_article';`,
  )

  // Navbar als erstes HTML-Element
  const navbarHtml = generateNavbarHtml(projectName)
  statements.push(
    `INSERT INTO tl_content (pid, ptable, type, html, sorting, tstamp) VALUES (1, 'tl_article', 'html', ${sqlEsc(navbarHtml)}, 64, UNIX_TIMESTAMP());`,
  )

  // Content-Elemente pro Section
  let sorting = 128
  for (const section of sections) {
    const html = `<section class="section section--${section.id}" id="${section.id}">\n${section.output}\n</section>`
    statements.push(
      `INSERT INTO tl_content (pid, ptable, type, html, sorting, tstamp) VALUES (1, 'tl_article', 'html', ${sqlEsc(html)}, ${sorting}, UNIX_TIMESTAMP());`,
    )
    sorting += 128
  }

  // Footer als letztes HTML-Element
  const footerHtml = generateFooterHtml(projectName)
  statements.push(
    `INSERT INTO tl_content (pid, ptable, type, html, sorting, tstamp) VALUES (1, 'tl_article', 'html', ${sqlEsc(footerHtml)}, ${sorting}, UNIX_TIMESTAMP());`,
  )

  return statements.join('\n')
}

