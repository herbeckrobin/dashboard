// JavaScript fuer Smooth Scroll, Navbar Scroll-Effekt, Mobile Menu
// Wird als separate Datei geschrieben (Contao-Sanitizer blockt <script> in Content-Elementen)
export function generateMainJs() {
  return `// Smooth scroll
document.querySelectorAll('a[href^="#"]').forEach(function(a) {
  a.addEventListener('click', function(e) {
    e.preventDefault();
    var el = document.querySelector(a.getAttribute('href'));
    if (el) el.scrollIntoView({ behavior: 'smooth' });
  });
});
// Navbar scroll effect
window.addEventListener('scroll', function() {
  var navbar = document.getElementById('navbar');
  if (navbar) navbar.classList.toggle('navbar--scrolled', window.scrollY > 50);
});
// Mobile menu toggle
var toggle = document.querySelector('.navbar__toggle');
if (toggle) {
  toggle.addEventListener('click', function() {
    document.querySelector('.navbar__menu').classList.toggle('navbar__menu--open');
  });
}
`
}

