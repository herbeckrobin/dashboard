// ============================================================
// CSS GENERATOR — Base CSS + per-Section CSS aus Registry
// ============================================================

import { getThemeDefaults, getFontConfig } from '../theme-defaults.js'
import { SECTION_CSS } from '../sections/index.js'

export function generateStyleCSS(themeConfig = {}) {
  const mode = themeConfig.mode || 'light'
  const defaults = getThemeDefaults(mode)
  const primary = themeConfig.primary || '#2563eb'
  const secondary = themeConfig.secondary || '#7c3aed'
  const bg = defaults.bg
  const surface = defaults.surface
  const surfaceAlt = defaults.surfaceAlt
  const text = defaults.text
  const textMuted = defaults.textMuted
  const textInverted = defaults.textInverted
  const border = defaults.border
  const fontConfig = getFontConfig(themeConfig.font)

  // Base CSS (Root-Variablen, Reset, Container, Navbar, Buttons, Section-Base, Footer, Animation)
  const baseCss = `/* Generated by Deploy Dashboard — Redaxo Theme */
:root {
  --color-primary: ${primary};
  --color-secondary: ${secondary};
  --color-bg: ${bg};
  --color-surface: ${surface};
  --color-surface-alt: ${surfaceAlt};
  --color-text: ${text};
  --color-text-muted: ${textMuted};
  --color-text-inverted: ${textInverted};
  --color-border: ${border};
  --font-body: ${fontConfig.body};
  --font-heading: ${fontConfig.heading};
  --radius: 12px;
  --shadow: 0 4px 20px rgba(0,0,0,0.08);
  --transition: 0.3s ease;
}

/* Reset + Base */
*, *::before, *::after { box-sizing: border-box; margin: 0; padding: 0; }
html { scroll-behavior: smooth; }
body {
  font-family: var(--font-body);
  background: var(--color-bg);
  color: var(--color-text);
  line-height: 1.6;
}
h1, h2, h3, h4, h5, h6 {
  font-family: var(--font-heading);
  letter-spacing: -0.02em;
}
a { color: var(--color-primary); text-decoration: none; }
img { max-width: 100%; height: auto; }

/* Container */
.container {
  max-width: 1200px;
  margin: 0 auto;
  padding: 0 1.5rem;
}

/* Navbar */
.navbar {
  position: fixed;
  top: 0;
  left: 0;
  right: 0;
  z-index: 1000;
  padding: 1rem 0;
  transition: all var(--transition);
  background: transparent;
}
.navbar--scrolled {
  background: ${mode === 'dark' ? 'rgba(10,10,10,0.95)' : 'rgba(255,255,255,0.95)'};
  backdrop-filter: blur(10px);
  box-shadow: 0 2px 20px rgba(0,0,0,0.1);
  padding: 0.5rem 0;
}
.navbar__inner {
  display: flex;
  align-items: center;
  justify-content: space-between;
}
.navbar__brand {
  font-size: 1.5rem;
  font-weight: 700;
  color: var(--color-text-inverted);
}
.navbar__menu {
  display: flex;
  list-style: none;
  gap: 2rem;
}
.navbar__menu a {
  color: rgba(255,255,255,0.85);
  font-weight: 500;
  transition: color var(--transition);
}
.navbar__menu a:hover { color: #fff; }
.navbar--scrolled .navbar__brand { color: var(--color-text); }
.navbar--scrolled .navbar__menu a { color: var(--color-text-muted); }
.navbar--scrolled .navbar__menu a:hover { color: var(--color-primary); }
.navbar__toggle {
  display: none;
  flex-direction: column;
  gap: 5px;
  background: none;
  border: none;
  cursor: pointer;
}
.navbar__toggle span {
  width: 24px;
  height: 2px;
  background: var(--color-text-inverted);
  transition: var(--transition);
}
.navbar--scrolled .navbar__toggle span { background: var(--color-text); }
@media (max-width: 768px) {
  .navbar__toggle { display: flex; }
  .navbar__menu {
    display: none;
    position: absolute;
    top: 100%;
    left: 0;
    right: 0;
    flex-direction: column;
    padding: 1rem 1.5rem;
    gap: 0.5rem;
    background: ${mode === 'dark' ? 'rgba(10,10,10,0.98)' : 'rgba(255,255,255,0.98)'};
    backdrop-filter: blur(10px);
  }
  .navbar__menu--open { display: flex; }
}

/* Buttons */
.btn {
  display: inline-block;
  padding: 0.75rem 2rem;
  border-radius: var(--radius);
  font-weight: 600;
  cursor: pointer;
  transition: all var(--transition);
  border: none;
  font-size: 1rem;
}
.btn--primary {
  background: var(--color-primary);
  color: var(--color-text-inverted);
}
.btn--primary:hover {
  opacity: 0.9;
  transform: translateY(-2px);
  box-shadow: 0 8px 25px rgba(0,0,0,0.15);
}
.btn--secondary {
  background: transparent;
  color: var(--color-primary);
  border: 2px solid var(--color-primary);
}
.btn--secondary:hover {
  background: var(--color-primary);
  color: var(--color-text-inverted);
  transform: translateY(-2px);
}
.section--hero .btn--secondary,
.section--beitreten .btn--secondary,
[style*="gradient"] .btn--secondary {
  color: var(--color-text-inverted);
  border-color: rgba(255,255,255,0.5);
}
.section--hero .btn--secondary:hover,
.section--beitreten .btn--secondary:hover {
  background: rgba(255,255,255,0.15);
  border-color: #fff;
}
.btn--lg, .btn--large { padding: 1rem 2.5rem; font-size: 1.1rem; }

/* Sections */
.section {
  padding: clamp(4rem, 8vw, 7rem) 0;
}
.section__title {
  font-size: clamp(1.75rem, 3vw, 2.75rem);
  font-weight: 700;
  font-family: var(--font-heading);
  margin-bottom: 3rem;
  text-align: center;
  letter-spacing: -0.02em;
}

/* Footer */
.footer {
  background: ${mode === 'dark' ? '#050505' : '#111827'};
  color: ${mode === 'dark' ? '#9ca3af' : '#d1d5db'};
  padding: 2rem 0;
  text-align: center;
}
.footer a { color: var(--color-primary); }

/* Scroll Animation */
@keyframes fadeInUp {
  from { opacity: 0; transform: translateY(30px); }
  to { opacity: 1; transform: translateY(0); }
}
.section { animation: fadeInUp 0.6s ease both; }
`

  // Per-Section CSS aus Registry zusammensetzen
  const sectionCss = Object.values(SECTION_CSS).join('\n')

  return baseCss + sectionCss
}
