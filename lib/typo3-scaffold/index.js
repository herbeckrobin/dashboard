// ============================================================
// SITE PACKAGE GENERATOR — TYPO3 Extension-Dateien
// ============================================================

export function generateSitePackage(projectName, themeCSS, googleFontsUrl = null) {
  const extKey = 'site_package'
  const fontsLink = googleFontsUrl
    ? `\n    <link rel="preconnect" href="https://fonts.googleapis.com">\n    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>\n    <link rel="stylesheet" href="${googleFontsUrl}">`
    : ''

  const files = []

  // ext_emconf.php
  files.push({
    path: 'ext_emconf.php',
    content: `<?php
$EM_CONF[$_EXTKEY] = [
    'title' => 'Site Package',
    'description' => 'Site package fuer ${projectName}',
    'version' => '1.0.0',
    'state' => 'stable',
    'constraints' => [
        'depends' => [
            'typo3' => '13.0.0-13.99.99',
            'fluid_styled_content' => '*',
        ],
    ],
];
`
  })

  // composer.json
  files.push({
    path: 'composer.json',
    content: JSON.stringify({
      name: 'site/package',
      type: 'typo3-cms-extension',
      description: `Site package fuer ${projectName}`,
      license: 'GPL-2.0-or-later',
      require: { 'typo3/cms-core': '^13' },
      autoload: { 'psr-4': { 'Site\\Package\\': 'Classes/' } },
      extra: { 'typo3/cms': { 'extension-key': extKey } },
    }, null, 2) + '\n'
  })

  // TypoScript setup
  files.push({
    path: 'Configuration/TypoScript/setup.typoscript',
    content: `page = PAGE
page {
    10 = FLUIDTEMPLATE
    10 {
        templateName = Default
        templateRootPaths.10 = EXT:${extKey}/Resources/Private/Templates/Page/
        layoutRootPaths.10 = EXT:${extKey}/Resources/Private/Layouts/Page/
        partialRootPaths.10 = EXT:${extKey}/Resources/Private/Partials/
    }
    includeCSS.theme = EXT:${extKey}/Resources/Public/Css/theme.css
    includeJSFooter.main = EXT:${extKey}/Resources/Public/JavaScript/main.js
}
`
  })

  // Fluid Layout
  files.push({
    path: 'Resources/Private/Layouts/Page/Default.html',
    content: `<f:render section="main" />\n`
  })

  // Fluid Template (Navbar + Content + Footer)
  files.push({
    path: 'Resources/Private/Templates/Page/Default.html',
    content: `<f:layout name="Default" />
<f:section name="main">
<header>${fontsLink}
</header>

<nav class="navbar" id="navbar">
  <div class="container navbar__inner">
    <a href="/" class="navbar__brand">${projectName}</a>
    <button class="navbar__toggle" aria-label="Menu">
      <span></span><span></span><span></span>
    </button>
    <ul class="navbar__menu">
      <li><a href="#hero">Start</a></li>
      <li><a href="#about">Ueber uns</a></li>
      <li><a href="#services">Leistungen</a></li>
      <li><a href="#kontakt">Kontakt</a></li>
    </ul>
  </div>
</nav>

<main>
  <f:cObject typoscriptObjectPath="styles.content.get" />
</main>

<footer class="footer">
  <div class="container">
    <p>&copy; <f:format.date format="Y">now</f:format.date> ${projectName}. Alle Rechte vorbehalten.</p>
  </div>
</footer>
</f:section>
`
  })

  // JavaScript (Smooth scroll + Navbar)
  files.push({
    path: 'Resources/Public/JavaScript/main.js',
    content: `// Smooth scroll
document.querySelectorAll('a[href^="#"]').forEach(function(a) {
  a.addEventListener('click', function(e) {
    e.preventDefault();
    var el = document.querySelector(a.getAttribute('href'));
    if (el) el.scrollIntoView({ behavior: 'smooth' });
  });
});
// Navbar scroll effect
window.addEventListener('scroll', function() {
  document.getElementById('navbar').classList.toggle('navbar--scrolled', window.scrollY > 50);
});
// Mobile menu toggle
var toggle = document.querySelector('.navbar__toggle');
if (toggle) {
  toggle.addEventListener('click', function() {
    document.querySelector('.navbar__menu').classList.toggle('navbar__menu--open');
  });
}
`
  })

  // Theme CSS (Platzhalter, wird mit generiertem CSS ersetzt)
  files.push({
    path: 'Resources/Public/Css/theme.css',
    content: themeCSS
  })

  return files
}

