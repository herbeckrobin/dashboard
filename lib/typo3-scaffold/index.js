// SQL GENERATOR — Content-Elemente + TypoScript-Template
// ============================================================

export function generateContentSQL(sections, projectName) {
  const statements = ['SET NAMES utf8mb4;']

  // sys_template Record erstellen (TypoScript fuer Root-Page)
  // config enthaelt direkt die TypoScript-Anweisungen
  const tsConfig = [
    'page = PAGE',
    'page.10 = FLUIDTEMPLATE',
    'page.10.templateName = Default',
    'page.10.templateRootPaths.10 = EXT:site_package/Resources/Private/Templates/Page/',
    'page.10.layoutRootPaths.10 = EXT:site_package/Resources/Private/Layouts/Page/',
    'page.10.partialRootPaths.10 = EXT:site_package/Resources/Private/Partials/',
    'page.includeCSS.theme = EXT:site_package/Resources/Public/Css/theme.css',
    'page.includeJSFooter.main = EXT:site_package/Resources/Public/JavaScript/main.js',
  ].join('\n')

  statements.push(
    `DELETE FROM sys_template WHERE pid = 1;`,
    `INSERT INTO sys_template (pid, title, root, clear, config, include_static_file, tstamp, crdate) VALUES (1, ${sqlEsc('Site Package')}, 1, 3, ${sqlEsc(tsConfig)}, ${sqlEsc('EXT:fluid_styled_content/Configuration/TypoScript/')}, UNIX_TIMESTAMP(), UNIX_TIMESTAMP());`
  )

  // Bestehende Content-Elemente auf Seite 1 loeschen
  statements.push(`DELETE FROM tt_content WHERE pid = 1;`)

  // HTML Content-Elemente einfuegen
  let sorting = 256
  for (const section of sections) {
    const html = `<section class="section section--${section.id}" id="${section.id}">\n${section.output}\n</section>`
    statements.push(
      `INSERT INTO tt_content (pid, CType, header, bodytext, sorting, colPos, tstamp, crdate) VALUES (1, 'html', ${sqlEsc(section.name)}, ${sqlEsc(html)}, ${sorting}, 0, UNIX_TIMESTAMP(), UNIX_TIMESTAMP());`
    )
    sorting += 256
  }

  return statements.join('\n')
}

